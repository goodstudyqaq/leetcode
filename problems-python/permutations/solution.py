class Solution:
    def permute(self, nums: List[int]) -> List[List[int]]:
        ans = []

        def dfs(cur, nums):
            if not nums:
                ans.append(cur)
                return
            for i in range(len(nums)):
                dfs(cur + [nums[i]], nums[:i] + nums[i + 1:])
        dfs([], nums)
        return ans
